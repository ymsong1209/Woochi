using System.Collections;
using System.Collections.Generic;
using TMPro;
using UnityEngine;
using UnityEngine.EventSystems;
using UnityEngine.UI;

/// <summary>
/// UI에서 스킬 아이콘을 나타냄
/// </summary>
[DisallowMultipleComponent]
public class SkillIcon : MonoBehaviour, IPointerEnterHandler, IPointerExitHandler
{
    public  Image       icon;
    public  Button      btn;

    private BaseSkill   skill = new BaseSkill();

    public void SetSkill(BaseSkill _skill)
    {
        if (_skill!=null)
        {
            btn.interactable = true;
            skill = _skill;
        }
        //_skill이 null일 경우 빈 skill로 초기화
        else
        {
            skill = new BaseSkill();
        }
        // TODO : 스킬 아이콘 스프라이트 지정
    }

    public void OnPointerEnter(PointerEventData eventData)
    {
        Debug.Log("Skill Name : " + skill.Name + "위에 마우스 올라감");
    }

    public void OnPointerExit(PointerEventData eventData)
    {
    }

    #region Getter, Setter
    public BaseSkill Skill => skill;
    #endregion
}
